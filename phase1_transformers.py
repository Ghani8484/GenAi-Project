"""Encoder Module"""

class TransformerEncoder(nn.Module):
    def __init__(self, vocab_size, d_model=64, n_heads=4, n_layers=2, max_len=16):
        super().__init__()
        self.embed = nn.Embedding(vocab_size, d_model)
        self.pos = nn.Parameter(torch.zeros(1, max_len, d_model))
        self.layers = nn.ModuleList([
            TransformerEncoderLayer(d_model, n_heads, d_model * 4)
            for _ in range(n_layers)
        ])

    def forward(self, x):
        x = self.embed(x) + self.pos[:, :x.size(1), :]
        for layer in self.layers:
            x = layer(x)
        return x

"""Decoder Module (with cross-attention)"""

class TransformerDecoderLayer(nn.Module):
    def __init__(self, d_model, n_heads, d_ff):
        super().__init__()
        self.self_attn = MaskedMultiHeadSelfAttention(d_model, n_heads)
        self.cross_attn = CrossAttention(d_model, n_heads)

        self.norm1 = nn.LayerNorm(d_model)
        self.norm2 = nn.LayerNorm(d_model)
        self.norm3 = nn.LayerNorm(d_model)

        self.ff = nn.Sequential(
            nn.Linear(d_model, d_ff),
            nn.ReLU(),
            nn.Linear(d_ff, d_model)
        )

    def forward(self, x, enc_out):
        x = self.norm1(x + self.self_attn(x))
        x = self.norm2(x + self.cross_attn(x, enc_out))
        x = self.norm3(x + self.ff(x))
        return x

class CrossAttention(nn.Module):
    def __init__(self, d_model, n_heads):
        super().__init__()
        assert d_model % n_heads == 0

        self.n_heads = n_heads
        self.d_head = d_model // n_heads

        self.q = nn.Linear(d_model, d_model)
        self.k = nn.Linear(d_model, d_model)
        self.v = nn.Linear(d_model, d_model)
        self.out = nn.Linear(d_model, d_model)

    def forward(self, x, enc_out):
        # x: (B, dec_len, D)
        # enc_out: (B, enc_len, D)

        B, T_dec, D = x.shape
        T_enc = enc_out.size(1)

        Q = self.q(x).view(B, T_dec, self.n_heads, self.d_head).transpose(1, 2)
        K = self.k(enc_out).view(B, T_enc, self.n_heads, self.d_head).transpose(1, 2)
        V = self.v(enc_out).view(B, T_enc, self.n_heads, self.d_head).transpose(1, 2)

        scores = torch.matmul(Q, K.transpose(-2, -1)) / math.sqrt(self.d_head)
        attn = torch.softmax(scores, dim=-1)

        context = torch.matmul(attn, V)
        context = context.transpose(1, 2).contiguous().view(B, T_dec, D)

        return self.out(context)

"""Full Encoder-Decoder Model"""

class Seq2SeqTransformer(nn.Module):
    def __init__(self, vocab_size, d_model=64, n_heads=4, n_layers=2, max_len=16):
        super().__init__()
        self.encoder = TransformerEncoder(vocab_size, d_model, n_heads, n_layers, max_len)
        self.decoder_embed = nn.Embedding(vocab_size, d_model)
        self.decoder_pos = nn.Parameter(torch.zeros(1, max_len, d_model))
        self.decoder_layers = nn.ModuleList([
            TransformerDecoderLayer(d_model, n_heads, d_model * 4)
            for _ in range(n_layers)
        ])
        self.fc = nn.Linear(d_model, vocab_size)

    def forward(self, enc_x, dec_x):
        enc_out = self.encoder(enc_x)
        x = self.decoder_embed(dec_x) + self.decoder_pos[:, :dec_x.size(1), :]
        for layer in self.decoder_layers:
            x = layer(x, enc_out)
        return self.fc(x)

"""Train Encoder-Decoder"""

seq2seq_losses = []   # ADD THIS LINE BEFORE THE LOOP

for epoch in range(40):
    seq_model.train()
    total_loss = 0

    for enc, din, dout in seq_loader:
        enc, din, dout = enc.to(device), din.to(device), dout.to(device)

        optimizer.zero_grad()
        logits = seq_model(enc, din)
        loss = criterion(logits.view(-1, len(vocab)), dout.view(-1))
        loss.backward()
        optimizer.step()

        total_loss += loss.item()

    avg_loss = total_loss / len(seq_loader)
    seq2seq_losses.append(avg_loss)   # STORE LOSS
    print(f"Epoch {epoch+1} | Loss: {avg_loss:.4f}")

import matplotlib.pyplot as plt

